package classwork1103;

public class Main01 {
    public static void main(String[] args) {
        // Объявляем целочисленную переменную и записываем в нее число 10
        int number1 = 10;
        // Объявляем вещественную переменную и записываем в нее число 5.25
        // Академически, это называется "Число с плавающей точкой", или "Floating point number"
        double number2 = 5.25;
        // Объявляем символьную переменную и записываем символ а
        char letter = 'a';
        // шаблон sout поможет быстро написать System.out.println();
        System.out.println(number1);
        System.out.println(number2);
        System.out.println(letter);

    }
}
